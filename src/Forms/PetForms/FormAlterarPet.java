    private void jButtonBUSCARActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonBUSCARActionPerformed
        // TODO add your handling code here:

        try {
            Pet filtro = new Pet();

            PetDado dados = new PetDado();
            this.listaPets = dados.listar(filtro);
            DefaultTableModel modelo = new DefaultTableModel();
            modelo.setColumnIdentifiers(new Object[]{"CODIGO","NOME","ESPECIE","IDADE","RACA"});
            for (Pet pet : listaPets) {
                modelo.addRow(new Object[]{pet.getID(), pet.getNome(), pet.getEspecie(), pet.getIdade(), pet.getRaca()});
            }
            jTableLISTAR.setModel(modelo);
            //JOptionPane.showMessageDialog(this, "Aluno cadastrado");
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
        }

    }//GEN-LAST:event_jButtonBUSCARActionPerformed

    private void jButtonEXCLUIRActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonEXCLUIRActionPerformed
        // TODO add your handling code here:

        try {
            Pet p = new Pet();

            p.setID(Integer.parseInt(jTextFieldCODIGO.getText()));

            PetDado negocio = new PetDado();
            negocio.DeletarPet(p);
            JOptionPane.showMessageDialog(this, "Pet excluido");

            TxtNome.setText("");
            TxtEspecie.setText("");
            TxtRaca.setText("");
            TxtIdade.setText("");

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
        }

    }//GEN-LAST:event_jButtonEXCLUIRActionPerformed

