    private void jButtonALTERARActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonALTERARActionPerformed
        // TODO add your handling code here:

        try {
            Pet p = new Pet();

            if (jTextFieldCODIGO.getText().matches("[0-9]+") == false) {
                JOptionPane.showMessageDialog(this, "Informe apenas números para o codigo");
                return;
            }
            p.setNome(TxtNome.getText());
            p.setEspecie(TxtEspecie.getText());
            p.setIdade(Integer.parseInt(TxtIdade.getText()));
            p.setRaca(TxtRaca.getText());

            PetNegocio negocio = new PetNegocio();
            negocio.atualizarPet(p);
            JOptionPane.showMessageDialog(this, "Pet alterado");

            TxtNome.setText("");
            TxtEspecie.setText("");
            TxtRaca.setText("");
            TxtIdade.setText("");

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
        }


    }//GEN-LAST:event_jButtonALTERARActionPerformed

